package com.bi.biproxyapi.model;

import java.math.BigDecimal;

public class ListaResultado {

	
	private String descricao;
	private BigDecimal mesAtual;
	private BigDecimal acumAtual;
	/**
	 * @return the descricao
	 */
	public String getDescricao() {
		return descricao;
	}
	/**
	 * @param descricao the descricao to set
	 */
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	/**
	 * @return the mesAtual
	 */
	public BigDecimal getMesAtual() {
		return mesAtual;
	}
	/**
	 * @param mesAtual the mesAtual to set
	 */
	public void setMesAtual(BigDecimal mesAtual) {
		this.mesAtual = mesAtual;
	}
	/**
	 * @return the acumAtual
	 */
	public BigDecimal getAcumAtual() {
		return acumAtual;
	}
	/**
	 * @param acumAtual the acumAtual to set
	 */
	public void setAcumAtual(BigDecimal acumAtual) {
		this.acumAtual = acumAtual;
	}
	
	
	
	
	
	
	
}
